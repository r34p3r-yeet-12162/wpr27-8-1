/* When we declare a function, we define how some input values will be used
A parameter is a placeholder for a future value that will be provided later
*/

function GreetUser(username) {
  // username is a PARAMETER (in place of a value)
  //console.log('Hi John');
  console.log("Hi John");
}
//  when we call a function, we then provide the actual value
// the actual value is called an ARGUMENT
GreetUser("Jack"); // the value JACK is an argument
